#include "Vector2.h"

void Vector2::SetPosition(float x_, float y_){

    x = x_;
    y = y_;
}

void Vector2::SetX(float x_){

    x = x_;
}

void Vector2::SetY(float y_){

    y = y_;
}

float Vector2::GetX(){

    return x;
}

float Vector2::GetY(){

    return y;
}
